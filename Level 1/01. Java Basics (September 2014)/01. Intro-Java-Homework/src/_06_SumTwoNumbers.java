import java.util.*;

class _06_SumTwoNumbers {
	
	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		System.out.print("Enter integer number a: ");
		int a = input.nextInt();
		System.out.print("Enter integer number b: ");
		int b = input.nextInt();

		System.out.println("Sum: " + (a + b));
	}
	
}